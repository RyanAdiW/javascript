function testingSwitch(){
	var nilai = 8;

	switch(nilai){
		case 10:
			console.log("sangat bagus");
		break;
		case 5:
			console.log("kurang, tingkatkan lagi");
		break;
		case 0:
			console.log("sangat kurang, belajar yang bener!");
		break;
		default:
			console.log("lainnya");
	}
}

testingSwitch();
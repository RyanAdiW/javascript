function ubahNilai(a) {
	//nilai sebelum dirubah
	console.log("nilai sebelum dirubah = ",a);
	
	//mengubah nilai dengan mengalikannya dengan 10
	var hasil = a * 10
	return hasil;
}
console.log("nilai setelah dirubah = ",ubahNilai(10));